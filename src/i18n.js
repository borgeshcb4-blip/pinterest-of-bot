/**
 * Detecta o idioma do usu√°rio baseado no language_code do Telegram
 * @param {string} languageCode - C√≥digo de idioma do Telegram (ex: 'pt', 'en', 'pt-BR', 'en-US')
 * @returns {string} Idioma suportado
 */
export function detectLanguage(languageCode) {
  if (!languageCode) return 'en';
  
  const code = languageCode.toLowerCase();
  
  // Verifica correspond√™ncia exata primeiro (ex: 'pt', 'en', 'es')
  if (translations[code]) {
    return code;
  }
  
  // Verifica pelo prefixo do idioma (ex: 'pt-BR' -> 'pt', 'zh-CN' -> 'zh')
  const prefix = code.split('-')[0];
  if (translations[prefix]) {
    return prefix;
  }
  
  // Mapeamentos especiais para variantes de idiomas
  const languageMappings = {
    'zh-cn': 'zh', 'zh-tw': 'zh', 'zh-hk': 'zh', 'zh-hans': 'zh', 'zh-hant': 'zh',
    'pt-br': 'pt', 'pt-pt': 'pt',
    'es-es': 'es', 'es-mx': 'es', 'es-ar': 'es', 'es-419': 'es',
    'en-us': 'en', 'en-gb': 'en', 'en-au': 'en',
    'fr-fr': 'fr', 'fr-ca': 'fr',
    'de-de': 'de', 'de-at': 'de', 'de-ch': 'de',
    'ar-sa': 'ar', 'ar-eg': 'ar', 'ar-ae': 'ar',
    'ru-ru': 'ru',
    'ja-jp': 'ja',
    'it-it': 'it',
    'ko-kr': 'ko',
    'tr-tr': 'tr',
    'hi-in': 'hi',
  };
  
  if (languageMappings[code]) {
    return languageMappings[code];
  }
  
  return 'en';
}

