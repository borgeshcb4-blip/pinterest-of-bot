/**
 * Retorna uma mensagem traduzida para o idioma do usu√°rio
 * @param {string} key - Chave da mensagem
 * @param {string} language - Idioma
 * @param {object} params - Par√¢metros para substitui√ß√£o (ex: {name: 'Jo√£o'})
 * @returns {string} Mensagem traduzida
 */
export function getLocalizedMessage(key, language, params = {}) {
  let message = translations[language]?.[key];
  
  if (!message) {
    console.warn(`Translation key not found: ${key} for language: ${language}`);
    message = translations['en']?.[key] || key;
  }
  
  // Substitui os placeholders {{param}} pelos valores
  Object.keys(params).forEach(param => {
    message = message.replace(new RegExp(`{{${param}}}`, 'g'), params[param]);
  });
  
  return message;
}

